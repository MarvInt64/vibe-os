/* Forward declarations for the inline math helpers defined below.
 * Required because C99 disallows implicit function declarations. */
static float mp3_cosf(float x);
static float mp3_pow2f(float x);
static float mp3_powf(float base, float exp);

/* ---- Freestanding math primitives ----------------------------------------
 *
 * VibeOS compiles userspace with -fno-builtin, so we cannot use <math.h> or
 * compiler built-ins.  The decoder only needs mp3_powf(2,x) with x in [-60,10]
 * and mp3_cosf(x).  We implement both with sufficient precision for audio work.
 */

/* cosf via minimax polynomial on [0, π/2], mirrored to full circle.
 * Max error < 2e-7 — well within float precision for audio. */
static float mp3_cosf(float x) {
    /* Reduce x to [0, 2π] */
    static const float PI2 = 6.28318530718f;
    static const float PI  = 3.14159265359f;
    static const float PI_2= 1.57079632679f;
    /* bring into [0, 2π] */
    while (x < 0.0f)    x += PI2;
    while (x >= PI2)    x -= PI2;
    /* use cos symmetry to map to [0, π/2] */
    int neg = 0;
    if (x > PI)  { x = PI2 - x; }
    if (x > PI_2){ x = PI - x; neg = 1; }
    /* Minimax polynomial for cos on [0, π/2]: Horner form, degree 8 */
    float x2 = x * x;
    float c = 1.0f + x2 * (-0.4999999963f
              + x2 * ( 0.0416666418f
              + x2 * (-0.0013888397f
              + x2 * ( 0.0000247609f
              + x2 * (-0.0000002605f)))));
    return neg ? -c : c;
}

/* mp3_powf(2, x) via bit-manipulation for the integer part + polynomial for
 * the fractional part.  Accurate to < 1 ULP for |x| < 127. */
static float mp3_pow2f(float x) {
    int   xi  = (int)x;
    float xf  = x - (float)xi;            /* fractional part in [0,1) */
    /* Horner polynomial for 2^xf on [0,1): max error < 2e-7 */
    float p = 1.0f + xf * (0.6931471805f
              + xf * (0.2402265069f
              + xf * (0.0555041086f
              + xf * (0.0096181292f
              + xf * (0.0013333559f
              + xf * (0.0001540353f))))));
    /* Multiply by 2^xi by adjusting IEEE 754 exponent field */
    union { float f; unsigned int u; } e;
    e.f = p;
    e.u += (unsigned int)(xi << 23);
    return e.f;
}

/* log2f via IEEE 754 exponent split + atanh series on the [1,2) mantissa.
 *
 * For mantissa m in [1,2): log2(m) = (2/ln2) · atanh(t), t = (m-1)/(m+1) ∈ [0,1/3].
 * The series atanh(t) = t + t³/3 + t⁵/5 + t⁷/7 + t⁹/9 converges fast on this
 * range (the t¹¹ term is < 6e-7), giving < 1e-7 absolute error — far better
 * than the earlier degree-4 polynomial, whose ~1e-3 error biased every
 * requantised coefficient (|is|^(4/3)) and audibly attenuated the output. */
static float mp3_log2f(float x) {
    if (x <= 0.0f) return 0.0f;
    union { float f; unsigned int u; } v;
    v.f = x;
    int e = (int)((v.u >> 23) & 0xFF) - 127;      /* base-2 exponent */
    v.u = (v.u & 0x007FFFFFu) | 0x3F800000u;       /* mantissa → [1,2) */
    float m  = v.f;
    float t  = (m - 1.0f) / (m + 1.0f);
    float t2 = t * t;
    float s  = t * (1.0f + t2 * (0.33333333f
                  + t2 * (0.2f + t2 * (0.14285714f + t2 * 0.11111111f))));
    return 2.88539008f * s + (float)e;
}

/* General powf: base^exp = 2^(exp * log2(base)).
 * The decoder calls this both as mp3_powf(2, x) AND as mp3_powf(|is|, 4/3)
 * for requantization, so it MUST honour the base.  An earlier version ignored
 * the base and always returned 2^exp, which collapsed every spectral
 * coefficient to 2^(4/3) ≈ 2.52 and produced near-silent, wrong output. */
static float mp3_powf(float base, float exp) {
    if (base == 2.0f) return mp3_pow2f(exp);   /* common fast path */
    if (base <= 0.0f) return 0.0f;
    return mp3_pow2f(exp * mp3_log2f(base));
}

